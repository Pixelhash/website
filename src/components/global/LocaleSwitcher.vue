<template>
    <!-- {{ initLocale() }} -->
    <div class="navbar-item has-dropdown is-hoverable">
      <a class="navbar-link">
        üåç {{ $t("messages.language") }}
      </a>

      <div class="navbar-dropdown">
        <a
        class="navbar-item"
        v-for="locale in locales"
        :key="locale.id"
        @click="setLocale(locale)"
        href="#">
        {{ getLanguageString(locale) }}
        </a>
      </div>
    </div>
</template>

<script>
import Vue from "vue";
import VueCookie from "vue-cookie";
Vue.use(VueCookie);

const localeStrings = {
  en: "üá∫üá∏ English",
  de: "üá©üá™ Deutsch"
};

Vue.config.lang = VueCookie.get("locale") || "en";

console.log(
  "Locale from cookie = " +
    Vue.config.lang +
    ": language = " +
    localeStrings[Vue.config.lang]
);

export default {
  name: "language-switcher",
  props: {
    locales: {
      type: Array,
      default: ["en"]
    },
    showFull: Boolean,
    dropup: Boolean,
    locLabel: {
      type: String,
      default: ""
    }
  },
  data: function() {
    return {
      activeLocale: Vue.config.lang
    };
  },
  computed: {
    dropdownLbl: function() {
      return this.locLabel ? this.locLabel : this.activeLocale;
    }
  },
  methods: {
    setLocale: function(locale) {
      Vue.config.lang = locale;
      this.activeLocale = locale;
      this.$cookie.set("locale", locale);
      this.$i18n.locale = Vue.config.lang;
      console.log(
        "New locale = " +
          Vue.config.lang +
          ": language = " +
          localeStrings[Vue.config.lang]
      );
    },
    getLanguageString: function(locale) {
      return this.showFull ? localeStrings[locale] : locale;
    }
  }
};
</script>
